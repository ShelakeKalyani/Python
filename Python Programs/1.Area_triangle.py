breadth = int(input("Enter a breadth of triangle = "))      #Accept breadth of triangle from user
height = int(input("Enter a height of triangle = "))        #Accept height of triangle from user

Area = 1/2*breadth*height                                   #Calculate Area of triangle

print("Area of triangle = {}".format(Area))                 #Print Area

